                    if (!form.checkValidity()) {
                        event.preventDefault();
                        event.stopPropagation();
                    }
                    form.classList.add('was-validated');
                }, false);
            });

            const alerts = document.querySelectorAll(".alert");
            if (alerts.length > 0) {
                setTimeout(() => {
                    alerts.forEach(el => {
                        const bsAlert = new bootstrap.Alert(el);
                        bsAlert.close();
                    });
                }, 4000);
